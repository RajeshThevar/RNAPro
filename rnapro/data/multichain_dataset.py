"""Multi-chain dataset for competition fine-tuning.

Extends BaseSingleDataset with RibonanzaNet2 tokenization for multi-entity
complexes (RNA + protein + DNA + ligands). Uses pre-computed bioassembly_dict
.pkl.gz files generated by preprocess_bioassembly.py.

The base class handles all multi-entity processing: cropping, featurization,
label extraction, and entity-specific loss weights. This subclass adds:
  - RibonanzaNet2 sequence tokenization (RNA tokens -> 0-3, non-RNA -> PAD=4)
  - RNA sequence string for the `seq` feature key
"""

from __future__ import annotations

from pathlib import Path
from typing import Any, Optional, Union

import torch
from biotite.structure import AtomArray

from rnapro.data.constants import RNA_STD_RESIDUES
from rnapro.data.dataset import BaseSingleDataset
from rnapro.data.msa_featurizer import MSAFeaturizer
from rnapro.data.tokenizer import TokenArray
from rnapro.utils.logger import get_logger

logger = get_logger(__name__)

# RibonanzaNet2 tokenizer: maps nucleotide single-letter codes to indices.
# Non-RNA tokens (protein, DNA, ligand) are mapped to PAD (4).
RIBONANZANET2_TOKENIZER = {
    "A": 0,
    "C": 1,
    "G": 2,
    "U": 3,
    "PAD": 4,
    "X": 5,
}

# Map three-letter RNA residue names to single-letter codes for tokenization
_RNA_RESNAME_TO_LETTER = {
    "A": "A", "G": "G", "C": "C", "U": "U",
    "ADE": "A", "GUA": "G", "CYT": "C", "URA": "U",
}
# Include standard RNA residues from constants
for _name, _letter in RNA_STD_RESIDUES.items():
    _RNA_RESNAME_TO_LETTER.setdefault(_name, _letter)


class MultiChainDataset(BaseSingleDataset):
    """Dataset for multi-chain complex training using bioassembly_dict .pkl.gz files.

    Extends BaseSingleDataset to add RibonanzaNet2 tokenization for the `tokenized_seq`
    feature used by the RNAPro model. Non-RNA entities (protein, DNA, ligand) receive
    PAD tokens (4), which produce near-zero features through the frozen RibonanzaNet2
    encoder and are gated out by learned modules.

    Args:
        bioassembly_dict_dir: Directory containing .pkl.gz bioassembly files.
        indices_fpath: Path to indices.csv with per-chain/interface entries.
        cropping_configs: Cropping configuration dict (crop_size, method_weights).
        msa_featurizer: Optional MSAFeaturizer for per-entity MSA features.
        template_featurizer: Optional template featurizer.
        name: Dataset name for logging.
        **kwargs: Additional BaseSingleDataset arguments.
    """

    def __init__(
        self,
        bioassembly_dict_dir: Union[str, Path],
        indices_fpath: Union[str, Path],
        cropping_configs: dict[str, Any],
        msa_featurizer: Optional[MSAFeaturizer] = None,
        template_featurizer: Optional[Any] = None,
        name: str = "multichain",
        **kwargs,
    ) -> None:
        # BaseSingleDataset.__init__ does len(pdb_list) which crashes on None.
        # Ensure pdb_list defaults to empty list if not provided.
        kwargs.setdefault("pdb_list", [])
        super().__init__(
            mmcif_dir=None,
            bioassembly_dict_dir=bioassembly_dict_dir,
            indices_fpath=indices_fpath,
            cropping_configs=cropping_configs,
            msa_featurizer=msa_featurizer,
            template_featurizer=template_featurizer,
            name=name,
            **kwargs,
        )

    def process_one(
        self, idx: int, return_atom_token_array: bool = False
    ) -> dict[str, Any]:
        """Process a single sample, adding RibonanzaNet2 tokenization.

        Delegates all multi-entity processing to BaseSingleDataset.process_one(),
        then:
          1. Adds `tokenized_seq` (int tensor [N_token]) and `seq` (string) to
             input_feature_dict.
          2. Flattens the dict to match the structure AF3Trainer.model_forward()
             expects: `coordinate` and `coordinate_mask` at the top level (not
             nested inside `label_dict`), and `basic` dict with `pdb_id` and N_*
             counters also at the top level.

        BaseSingleDataset returns::

            {"input_feature_dict": {...}, "label_dict": {"coordinate": ...,
             "coordinate_mask": ...}, "label_full_dict": {...}, "basic": {...}}

        AF3Trainer.model_forward() expects::

            {"input_feature_dict": {...}, "coordinate": tensor,
             "coordinate_mask": tensor, "basic": {"pdb_id": ...}, ...}

        Args:
            idx: Sample index.
            return_atom_token_array: Whether to return raw arrays.

        Returns:
            Flat dict compatible with AF3Trainer.model_forward().
        """
        # Get the full processed data from parent (nested structure)
        nested = super().process_one(idx, return_atom_token_array=True)

        cropped_token_array = nested.pop("cropped_token_array")
        cropped_atom_array = nested.pop("cropped_atom_array")

        # Build RibonanzaNet2 tokenized sequence from cropped arrays
        seq_str, tokenized_seq = self._build_ribonanzanet2_tokens(
            cropped_token_array, cropped_atom_array
        )

        feat = nested["input_feature_dict"]
        feat["seq"] = seq_str
        feat["tokenized_seq"] = tokenized_seq

        # Flatten to match AF3Trainer.model_forward() expectations:
        # coordinate and coordinate_mask must be top-level keys.
        label_dict = nested.get("label_dict", {})
        basic = nested.get("basic", {})

        data: dict[str, Any] = {"input_feature_dict": feat}

        # Hoist coordinate tensors from label_dict to top level
        if "coordinate" in label_dict:
            data["coordinate"] = label_dict["coordinate"]
        if "coordinate_mask" in label_dict:
            data["coordinate_mask"] = label_dict["coordinate_mask"]

        # Hoist basic info (pdb_id, N_* counters) to top level
        data["basic"] = basic

        # Copy any remaining N_* counters from basic to top level for
        # compatibility (model_forward doesn't use these but other code might)
        for key in list(basic.keys()):
            if key.startswith("N_"):
                data[key] = basic[key]

        if return_atom_token_array:
            data["cropped_atom_array"] = cropped_atom_array
            data["cropped_token_array"] = cropped_token_array

        return data

    @staticmethod
    def _build_ribonanzanet2_tokens(
        token_array: TokenArray,
        atom_array: AtomArray,
    ) -> tuple[str, torch.Tensor]:
        """Build RibonanzaNet2 tokenized sequence from cropped token/atom arrays.

        For each token (residue-level for polymers, atom-level for ligands):
          - If the token belongs to an RNA chain: map its canonical residue name
            to a single letter (A/C/G/U), or 'X' if unknown
          - Otherwise: map to 'PAD'

        Args:
            token_array: Cropped TokenArray with centre_atom_index annotations.
            atom_array: Cropped AtomArray with mol_type and cano_seq_resname annotations.

        Returns:
            Tuple of (seq_string, tokenized_seq_tensor).
        """
        centre_atom_indices = token_array.get_annotation("centre_atom_index")
        n_tokens = len(centre_atom_indices)

        seq_chars: list[str] = []
        token_ids: list[int] = []

        for i in range(n_tokens):
            atom_idx = centre_atom_indices[i]
            mol_type = atom_array.mol_type[atom_idx]

            if mol_type == "rna":
                # Use cano_seq_resname for standard mapping
                resname = atom_array.cano_seq_resname[atom_idx]
                letter = _RNA_RESNAME_TO_LETTER.get(resname, "X")
                seq_chars.append(letter)
                token_ids.append(RIBONANZANET2_TOKENIZER.get(letter, 5))
            else:
                # Protein, DNA, ligand -> PAD
                seq_chars.append("PAD")
                token_ids.append(RIBONANZANET2_TOKENIZER["PAD"])

        seq_str = "".join(
            c if c != "PAD" else "X" for c in seq_chars
        )

        tokenized_seq = torch.tensor(token_ids, dtype=torch.long)
        return seq_str, tokenized_seq
